// i18n functionality
let currentLang = localStorage.getItem('language') || 'sr';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('language', lang);
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            // Check if element is input/textarea (use placeholder)
            if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                element.placeholder = translations[lang][key];
            } else if (element.tagName === 'SELECT') {
                // For select, update the first option
                const firstOption = element.querySelector('option[value=""]');
                if (firstOption) {
                    firstOption.textContent = translations[lang][key];
                }
            } else {
                // For regular elements, use innerHTML to support <br> tags
                element.innerHTML = translations[lang][key];
            }
        } else {
            // Translation key not found
        }
    });
    
    // Update all elements with data-i18n-placeholder attribute
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        const key = element.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            element.placeholder = translations[lang][key];
        }
    });
    
    // Update language buttons active state (including cloned mobile buttons)
    document.querySelectorAll('.lang-btn').forEach(btn => {
        const btnLang = btn.getAttribute('data-lang');
        if (btnLang === lang) {
            btn.classList.add('active');
            btn.style.background = 'linear-gradient(135deg, rgba(212,175,55,0.25), rgba(212,175,55,0.15))';
            btn.style.borderColor = 'rgba(212,175,55,0.4)';
            btn.style.color = '#FFD700';
            btn.style.fontWeight = '600';
        } else {
            btn.classList.remove('active');
            btn.style.background = 'transparent';
            btn.style.borderColor = 'rgba(255,255,255,0.12)';
            btn.style.color = 'rgba(255,255,255,0.6)';
            btn.style.fontWeight = '500';
        }
    });
    
    // Update cloned buttons in mobile menu switcher (if exists)
    document.querySelectorAll('.mobile-lang-switcher .lang-btn').forEach(btn => {
        const btnLang = btn.getAttribute('data-lang');
        if (btnLang === lang) {
            btn.classList.add('active');
            btn.style.background = 'linear-gradient(135deg, rgba(212,175,55,0.25), rgba(212,175,55,0.15))';
            btn.style.borderColor = 'rgba(212,175,55,0.4)';
            btn.style.color = '#FFD700';
            btn.style.fontWeight = '600';
        } else {
            btn.classList.remove('active');
            btn.style.background = 'transparent';
            btn.style.borderColor = 'rgba(255,255,255,0.12)';
            btn.style.color = 'rgba(255,255,255,0.6)';
            btn.style.fontWeight = '500';
        }
    });
    
    // Hide/show blog links based on language (blog is Serbian-only)
    const blogLinks = document.querySelectorAll('a[href*="blog.html"], a[href*="blog-post-"]');
    blogLinks.forEach(link => {
        if (lang === 'en' || lang === 'ru') {
            // Hide blog links for English and Russian
            link.style.display = 'none';
        } else {
            // Show blog links for Serbian
            link.style.display = '';
        }
    });
}

// Initialize language system
document.addEventListener('DOMContentLoaded', () => {
    // Set initial language
    setLanguage(currentLang);
    
    // Add click handlers to language buttons
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = btn.getAttribute('data-lang');
            setLanguage(lang);
        });
    });
});

// Backup: If DOMContentLoaded already fired, execute immediately
if (document.readyState === 'loading') {
    // Still loading, DOMContentLoaded will fire
} else {
    // DOM is already loaded, execute now
    setLanguage(currentLang);
    
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = btn.getAttribute('data-lang');
            setLanguage(lang);
        });
    });
}
